const os = require('os');

// Platform
console.log(os.platform());

// CPU Arch
console.log(os.arch());

// CPU core Info
console.log(os.cpus());

// Free Memo
console.log(os.freemem());

// Total Memo
console.log(os.totalmem())

// Home dir
console.log(os.homedir());

// Uptime
console.log(os.uptime());
